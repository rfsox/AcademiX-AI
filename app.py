@app.route('/generate_mcq', methods=['POST'])
def generate_mcq():
    try:
        # قمنا بزيادة كمية النص المستخلص لكي يقرأ الموديل الملزمة كاملة
        raw_text = extract_content(request.files['file'])
        
        completion = client.chat.completions.create(
            # استخدام الموديل الأضخم لضمان الدقة الأكاديمية
            model="llama-3.3-70b-versatile", 
            messages=[
                {"role": "system", "content": """أنت خبير أكاديمي متخصص في وضع الاختبارات المركزية والوزارية. 
                مهمتك هي استخراج أكبر عدد ممكن من أسئلة الـ MCQ (بحدود 50 إلى 100 سؤال) من النص المرفق.
                
                يجب الالتزام التام بالمنهج المذكور في النص وبدقة علمية 100%.
                
                قواعد التنسيق الإجبارية (ثنائية اللغة):
                1. السؤال: اكتب السؤال بالإنجليزية (EN) وتحته ترجمته الدقيقة بالعربية (AR).
                2. الاختيارات: اكتب الاختيار (A, B, C, D) بالإنجليزية وبجانبه ترجمته بالعربية في نفس السطر.
                3. الإجابة: اذكر الجواب الصحيح بوضوح.
                
                نمط الكتابة:
                EN: [The Question Text]
                AR: [نص السؤال بالعربية]
                A) [Option EN] / [الخيار بالعربي]
                B) [Option EN] / [الخيار بالعربي]
                C) [Option EN] / [الخيار بالعربي]
                D) [Option EN] / [الخيار بالعربي]
                Answer: [Correct Option]
                --------------------------------------------------
                ملاحظة: استخرج أسئلة من كل فقرة ومن كل صفحة في المنهج ولا تترك أي تفاصيل مهمة."""},
                {"role": "user", "content": f"أنشئ أكبر عدد ممكن من الأسئلة المترجمة بناءً على هذا المنهج:\n\n{raw_text[:25000]}"} # رفعنا سعة النص لـ 25 ألف حرف
            ],
            temperature=0.4, # تقليل الحرارة لضمان الدقة وعدم التأليف
            max_tokens=8000  # رفع التوكنز لأقصى حد للسماح بكتابة عدد كبير من الأسئلة
        )
        return jsonify({'questions': completion.choices[0].message.content})
    except Exception as e: return jsonify({'error': str(e)}), 500
